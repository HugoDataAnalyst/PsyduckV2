import asyncio
import time
from utils.logger import logger
from my_redis.connect_redis import RedisManager
from my_redis.queries.buffer.pokemon_bulk_buffer import PokemonIVRedisBuffer

class PokemonIVBufferFlusher:
    def __init__(self, flush_interval: int = 300):
        self.flush_interval = flush_interval
        self._running = False
        self._task = None

    async def flush_loop(self):
        """Periodically flush the Pok√©mon IV buffer."""
        self._running = True
        await asyncio.sleep(5)  # Initial delay
        logger.info(f"‚è≥ Starting Pok√©mon IV aggregated buffer flusher every {self.flush_interval}s")

        while self._running:
            try:
                redis = await RedisManager.check_redis_connection("flush_heatmap_pool")
                if not redis:
                    logger.warning("‚ö†Ô∏è Redis not ready. Skipping flush cycle.")
                    await asyncio.sleep(self.flush_interval)
                    continue

                start = time.perf_counter()
                # Force flush regardless of threshold every 5 minutes
                await PokemonIVRedisBuffer.flush_if_ready(redis)
                duration = time.perf_counter() - start

                logger.success(f"üëª Completed aggregated Pok√©mon heatmap flush in {duration:.2f}s ‚è±Ô∏è")
            except asyncio.CancelledError:
                logger.info("üõë Pok√©mon IV buffer flusher loop was cancelled.")
                break
            except Exception as e:
                logger.error(f"‚ùå Exception in aggregated buffer flusher loop: {e}")
            finally:
                await asyncio.sleep(self.flush_interval)

    async def start(self):
        """Start the flush loop."""
        if self._running:
            logger.warning("‚ö†Ô∏è Pok√©mon IV buffer flusher is already running.")
            return

        self._task = asyncio.create_task(self.flush_loop())
        logger.info("üöÄ Started Pok√©mon IV buffer flusher.")

    async def stop(self):
        """Stop the flush loop."""
        if not self._running:
            logger.warning("‚ö†Ô∏è Pok√©mon IV buffer flusher is not running.")
            return

        self._running = False
        if self._task:
            self._task.cancel()
            try:
                await self._task
            except asyncio.CancelledError:
                logger.info("üõë Pok√©mon IV buffer flusher stopped.")
            except Exception as e:
                logger.error(f"‚ùå Error stopping Pok√©mon IV buffer flusher: {e}")
